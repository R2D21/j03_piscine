#include <stdio.h>
#include <stdlib.h>
#include <string.h>

char		*ft_strrev(char *str)
{
	int		i;
	int		l;
	char	t;

	l = 0;
	while (str[l] != '\0')
		l++;
	i = -1;
	while (++i < --l)
	{
		t = str[i];
		str[i] = str[l];
		str[l] = t;
		printf("%c", str[i]);
	}
	return (str);
}
