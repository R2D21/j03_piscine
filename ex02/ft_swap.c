#include <stdio.h>

void	ft_swap(int *a, int *b)
{
	int *tmp;

	printf("%i ; %i\n", *a, *b);
	tmp = a;
	a = b;
	b = tmp;
	printf("%i ; %i\n", *a, *b);
}
